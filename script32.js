// 5. Интернет-магазин занимается продажей различных сувениров и безделушек. Каждый сувенир весит 75 г, а безделушка – 112 г. Напишите программу, запрашивающую у пользователя количество тех и других покупок, после чего выведите на экран общий вес посылки.

const SOUVENIR_WEIGHT = 75;
const TRINKET_WEIGHT = 112;

let souvenir = Number(prompt("Введите кол-во сувениров"));
let trinket = Number(prompt("Введите кол-во бездлушек"));

let totalWeight = souvenir * SOUVENIR_WEIGHT + (trinket + TRINKET_WEIGHT);

document.write(`Общий вес составляет: ${totalWeight}г. <br>`); 




// 6. Представьте, что вы открыли в банке сберегательный счет под 4 % годовых. Проценты банк рассчитывает в конце года и добавляет к сумме счета. Напишите программу, которая запрашивает у пользователя сумму первоначального депозита, после чего рассчитывает и выводит на экран сумму на счету в конце первого, второго и третьего годов. Все суммы должны быть округлены до двух знаков после запятой.

