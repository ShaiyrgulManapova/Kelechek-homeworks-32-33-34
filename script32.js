// 1. Создайте программу, запрашивающую у пользователя длину и ширину садового участка в футах. Выведите на экран площадь участка в акрах. Подсказка. В одном акре содержится 43 560 квадратных футов.

const SQ_FEET_PER_ACRE = 43560;

let areaHeight = prompt('Введите высоту площади в футах');
let areaWidth = prompt('Введите ширину площади в футах');

let acres = (areaHeight * areaWidth) / SQ_FEET_PER_ACRE;
document.write(`<h1>Площадь составляет: ${acres.toFixed(2)} акров</h1> <br>`);



// 2. Во многих странах в стоимость стеклотары закладывается определенный депозит, чтобы стимулировать покупателей напитков сдавать пустые бутылки. Допустим, бутылки объемом 1 литр и меньше стоят $0,10, а бутылки большего объема – $0,25. Напишите программу, запрашивающую у пользователя количество бутылок каждого размера. На экране должна отобразиться сумма, которую можно выручить, если сдать всю имеющуюся посуду. Отформатируйте вывод так, чтобы сумма включала два знака после запятой и дополнялась слева символом доллара.

const LESS_DEPOSIT = 0.1;
const MORE_DEPOSIT = 0.25;

let numberOfSmallBottles = prompt('Введите количество бутылок объемом 1 литр и меньше');
let numberOfBigBottles = prompt('Введите количество бутылок объемом 1 литр и больше');

let refund = (numberOfSmallBottles * LESS_DEPOSIT) + (numberOfBigBottles * MORE_DEPOSIT);
document.write(` Выручка от сданных бутылок составляет: ${refund.toFixed(2)}$ <br>`);




// 3. Программа, которую вы напишете, должна начинаться с запроса у пользователя суммы заказа в ресторане. После этого должен быть произведен расчет налога и чаевых официанту. Вы можете использовать принятую в вашем регионе налоговую ставку для подсчета суммы сборов. В качестве чаевых мы оставим 18 % от стоимости заказа без учета налога. На выходе программа должна отобразить отдельно налог, сумму чаевых и итог, включая обе составляющие. Форматируйте вывод таким образом, чтобы все числа отображались с двумя знаками после запятой.

let sumOfOrder = prompt('Введите сумму заказа');
